    public static void accept() {
        if (SetupForm != null && SetupForm.AgreementForm != null) { // Pastikan SetupForm dan AgreementForm tidak null
            FlatAnimatedLafChange.showSnapshot();
            SetupForm.setContentPane(SetupForm.AgreementForm);
            SetupForm.AgreementForm.applyComponentOrientation(SetupForm.getComponentOrientation());
            SwingUtilities.updateComponentTreeUI(SetupForm.AgreementForm);
            FlatAnimatedLafChange.hideSnapshotWithAnimation();
        } else {
            System.err.println("Error: SetupApp or AgreementForm is null.");
        }
    }
    

